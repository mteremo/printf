#include "printf.h"

int	ft_putnbr(int n)
{
	long int	ln;

	ln = n;
	if (ln < 0)
	{
		write(1, "-", 1);
		ln = -ln;
	}
	if (ln > 9)
	{
		ft_putnbr(ln / 10);
	}
	ln = ln % 10 + '0';
	write(1, &ln, 1);
}
